    public function creating(Order $order)
    {
        $invoices = Invoice::where('client_id', $order->client_id)
            ->where('agent_id', $order->agent_id)
            ->where('created_at', Carbon::now()->subDays(3))
            ->get();
        if (count($invoices)) {
            return false;
        }
    }

